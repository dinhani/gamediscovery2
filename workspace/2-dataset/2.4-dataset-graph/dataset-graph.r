# ==============================================================================
# LIBRARIES
# ==============================================================================
source("../../libraries.r", encoding = "UTF-8")
library(igraph)

# ==============================================================================
# READ GAMES
# ==============================================================================
games = readRDS("../2.2-dataset-recoder/data/games.rds")

# ==============================================================================
# GENERATE GRAPH
# ==============================================================================
g = games %>%
  gather(Attribute, Value, -c(ID, Name)) %>%
  filter(sapply(Value, negate(is.null))) %>%
  unnest() %>%
  select(Name, Value) %>%
  graph_from_data_frame(directed = FALSE)